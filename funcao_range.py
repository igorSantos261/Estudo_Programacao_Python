#definir intervalo com RANGE()
for b in range(1,10):
     print(b)
print("...Fim... ")


#definir intervalo com RANGE(inicio, parada e incremento)
for j in range(1,10,2):
     print(j)
print("...Fim... ")

#Verificando indice com string

string=input('Entre com uma string : ')
